export async function loadImages(): Promise<StaticImageFile[]> {
  // 檢查是否在靜態環境中（沒有 API）
  if (typeof window !== 'undefined' && window.location.protocol === 'file:') {
    // 文件協議，使用靜態數據
    return staticImages;
  }
  
  // 嘗試使用 API，失敗則回退到靜態數據
  try {
    return await loadImagesFromAPI();
  } catch (error) {
    console.log("使用靜態圖片數據");
    return staticImages;
  }
}
