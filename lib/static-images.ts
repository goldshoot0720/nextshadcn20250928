// 動態載入圖片的函數（用於開發環境或有 API 的環境）
export async function loadImagesFromAPI(): Promise<StaticImageFile[]> {
  try {
    const response = await fetch("/api/images");
    const data = await response.json();
    if (data.success) {
      return data.images;
    }
    return [];
  } catch (error) {
    console.error("API 載入失敗，使用靜態數據:", error);
    return staticImages;
  }
}

